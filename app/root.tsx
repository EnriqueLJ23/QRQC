export async function loader() {
  // Primer request: garantiza esquema/seed de la BD y arranca los jobs
  const { getPool } = await import("~/lib/server/db.server");
  const { iniciarJobs } = await import("~/lib/server/jobs.server");
  await getPool();
  iniciarJobs();
  return null;
}

